void TftLcd::drawBoard(const array<array<Piece, 8>, 8> &board){
	for(int i = 0; i < 8; ++i){
		for(int j = 0; j < 8; ++j){
			drawPiece(i, j, board[i][j]);
		}
	}
}

void TftLcd::drawPiece(int x, int y, Piece p, bool select){
	Color front = Controller::isWhite(p) ? WHITE : BLACK;
	Color back = select ? YELLOW :
			     (x + y) % 2 == 0 ? GREEN : GREY;
	uint16_t bx = x * 30;
	uint16_t by = y * 30 + 69;
	auto pixels = dict[p];
	int k = 0;
	while(k < 900){
		if(k % 30 == 0){
			setCursor(bx, by);
			writeReg(0x2C);
			--by;
		}
		writeData((*pixels)[k] ? front : back);
		++k;
	}
}

void TftLcd::fill(uint16_t bx, uint16_t by, uint16_t ex,
		          uint16_t ey, Color color){
	for(int i = by; i <= ey; ++i){
		setCursor(bx, i);
		writeReg(0x2C);
		for(int j = bx; j <= ex; ++j){
			writeData(color);
		}
	}
}

